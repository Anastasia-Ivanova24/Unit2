public class Unit2Task0 {
    public static void main(String[] args) {
        int c = 0;
        int i = 0;

        for (int b = 0; b < 10; b++) {
            System.out.print(b + " ");
        }

        do {
            System.out.print(i + " ");
            i++;
        } while (i < 10);

        while (c < 10) {
            System.out.print(c + " ");
            c++;
        }
    }
}
